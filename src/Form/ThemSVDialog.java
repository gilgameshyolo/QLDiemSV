    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        SinhVien sv = new SinhVien();
        sv.setMaSV(txtMaSV.getText());
        sv.setTenSV(txtTenSV.getText());
        sv.setNgaysinh(txtNgaySinh.getText());
        if(cboGT.getSelectedItem()=="Nam")
        {
            sv.setGioitinh(false);
        }
        else{
            sv.setGioitinh(true);
        }
        sv.setDiachi(txtDC.getText());
        sv.setMaLop((int) v.get(cboLop.getSelectedIndex()));
        if(txtMaSV.toString()==""){
        JOptionPane.showMessageDialog(null, "Mã môn học không được để trống");
        }
        if(txtTenSV.toString()==""){
        JOptionPane.showMessageDialog(null, "Tên môn học không được để trống");
        }
        else
        {
            if(svDAO.themMoi(sv))
                JOptionPane.showMessageDialog(rootPane, "Thêm mới thành công");
            else
                JOptionPane.showMessageDialog(rootPane, "Thêm mới thất bại");
        }
        this.dispose();
    }//GEN-LAST:event_btnThemActionPerformed

