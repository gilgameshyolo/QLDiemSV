    private void loadcboLop(){
        List<Lop> listlop = lopDAO.getAll();
        for (int i = 0; i < listlop.size(); i++) {
            Lop l = listlop.get(i);
            v.add(i, l.getMaLop());
            cboLop.addItem(l.getTenLop());
            
        }
    }
    
