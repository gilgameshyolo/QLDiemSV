        public void loadDSSV(){
        JTable table = new JTable();
        DefaultTableModel tbl = new DefaultTableModel();
        Object rows[] = new Object[6];
        Object cols[] = new Object[6];
        cols[0] = "Mã sinh viên";
        cols[1] = "Tên sinh viên";
        cols[2] = "Ngày sinh";
        cols[3] = "Giới tính";
        cols[4] = "Địa chỉ";
        cols[5] = "Lớp";
        
        tbl.setColumnIdentifiers(cols);
        
        List<SinhVien> listSV = svDAO.getAll();
        List<Lop> listLop = lopDAO.getAll();
        for (int i = 0; i < listSV.size(); i++) {
            SinhVien sv = listSV.get(i);
            rows[0] = sv.getMaSV();
            rows[1] = sv.getTenSV();
            rows[2] = sv.getNgaysinh();
            if(sv.getGioitinh()==false)
                rows[3] = "Nam";
            else
                rows[3] = "Nữ";
            rows[4] = sv.getDiachi();
            for (int j = 0; j < listLop.size(); j++) {
                Lop l = listLop.get(j);
                if(sv.getMaLop()==l.getMaLop())
                rows[5] = l.getTenLop();
            }
            
            
            tbl.addRow(rows);
        }
        tblDSSV.setModel(tbl);
    }
        
    private void loadcboMon(){
        List<MonHoc> listmon = monDAO.getAll();
        for (int i = 0; i < listmon.size(); i++) {
            MonHoc mh = listmon.get(i);
            a.add(i, mh.getMaMH());
            cboMon.addItem(mh.getTenMH());

        }
    }
    
    private void loadcboLoai(){
        List<LoaiDiem> listld = ldDAO.getAll();
        for (int i = 0; i < listld.size(); i++) {
            LoaiDiem ld = listld.get(i);
            b.add(i, ld.getMaLoai());
            cboLoai.addItem(ld.getTenLoai());

        }
    }


