    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        Diem d = new Diem();
        if(tblDSSV.getSelectedRowCount()==0){
            JOptionPane.showMessageDialog(rootPane, "Mời chọn lại");}
        else{
            int row = tblDSSV.getSelectedRow();
            String ma = tblDSSV.getValueAt(row, 0).toString();
            d.setMaSV(ma);
            d.setMaMH((int) a.get(cboMon.getSelectedIndex()));
            d.setMaLoai((int) b.get(cboLoai.getSelectedIndex()));
            d.setDiem(Integer.parseInt(txtDiem.getText()));
            if(txtDiem.toString()==""){
                JOptionPane.showMessageDialog(null, "Nhập lại điểm");
            }
            else{
                if(diemDAO.themMoi(d)){
                    JOptionPane.showMessageDialog(rootPane, "Thêm mới thành công");
                     }
                else
                    JOptionPane.showMessageDialog(rootPane, "Thêm mới thất bại");
                }
        }
    }//GEN-LAST:event_btnThemActionPerformed

